<?php

namespace Database\Seeders;

use App\Models\TechProfile;
use Illuminate\Database\Seeder;

/**
 * Справочник технологии (направления + виды практик), источник olymp.odkzn.ru.
 * Идемпотентно: совпадение направления по имени, практики — по (направление, код).
 */
class TechReferenceSeeder extends Seeder
{
    private const DATA = [
        ['Техника, технологии и техническое творчество', [
            ['1.1', 'Практика по ручной деревообработке'],
            ['1.2', 'Практика по механической деревообработке'],
            ['1.3', 'Практика по ручной металлообработке'],
            ['1.4', 'Практика по механической металлообработке'],
            ['1.5', 'Электрорадиотехника'],
            ['1.6', '3D-моделирование и печать'],
            ['1.7', 'Практика по работе на лазерно-гравировальном станке'],
            ['1.8', 'Промышленный дизайн'],
        ]],
        ['Культура дома, дизайн и технологии', [
            ['2.1', 'Ручная обработка швейного изделия или узла'],
            ['2.2', 'Механическая обработка швейного изделия или узла + Моделирование'],
            ['2.3', 'Обработка швейного изделия или узла на швейно-вышивальном оборудовании + Моделирование'],
            ['2.4', '3D-моделирование и печать'],
            ['2.5', 'Практика по работе на лазерно-гравировальном станке'],
            ['2.6', 'Промышленный дизайн'],
        ]],
    ];

    public function run(): void
    {
        foreach (self::DATA as $pi => [$profileName, $practices]) {
            $profile = TechProfile::updateOrCreate(
                ['name' => $profileName],
                ['position' => $pi + 1, 'is_active' => true],
            );

            foreach ($practices as $idx => [$code, $name]) {
                $profile->practices()->updateOrCreate(
                    ['code' => $code],
                    ['name' => $name, 'position' => $idx + 1, 'is_active' => true],
                );
            }
        }
    }
}
